/*////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
var $ = jQuery.noConflict();
/*////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
/*//////////////////// Variables End                                                                                      */
/*////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/



/*////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
/*//////////////////// Document Ready Function Starts                                                                     */
/*////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
jQuery(document).ready(function($){
				
			
				
	// scroll to function starts	
	$('.menuItem').click(function() {
											  	
		var goTo = $(this).attr('href');
	    
	    var scrollVal = $(goTo).offset().top;
		
		$('html, body').stop(true, true).animate({scrollTop: scrollVal-25}, 1200, 'easeOutCubic', function(){window.location.hash = goTo;});
			
		return false;
	
	});
	//scroll to function ends
	
	
	
	// back to top button function starts
	$('.backToTopButton').click(function() {
	
		$('html, body').stop(true, true).animate({scrollTop: '0px'}, 1200, 'easeOutCubic', function(){window.location.hash = '';});
		
		return false;
	
	});					
	// back to top button function ends 
		
			
			
}); 
/*////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
/*//////////////////// Document Ready Function Ends                                                                       */
/*////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/
